%--------------------------------------------------------------------------
%   多普勒时域平移测试
%--------------------------------------------------------------------------
clear;clc;

sig1 = rt.exp_wave(1e-6,1e7,1e8);
sig1 = [sig1;zeros(1000,1)];

Nfft = 2048;
n = (0:2047)';

%--------------------------------------------------------------------------
%   频域平移点数对应时域
%--------------------------------------------------------------------------
move_point = 200;

wl = -2*pi*n/Nfft;
sig_f = fft(sig1,Nfft);

%--------------------------------------------------------------------------
%   频域上平移点数和时域的关系
%--------------------------------------------------------------------------
sig_f2 = sig_f.*exp(1j.*wl*move_point);
sig2 = ifft(sig_f2,Nfft);
sig2 = sig2(1:size(sig1,1));

plot(abs(sig1));hold on
plot(abs(sig2));hold off
